    <p data-lang-id="001-richard-passing-notice">It is with a heavy heart that we share the news of our friend and colleague Richard Goldsmith's passing. Richard, one of our UK trainers, was known to many of you through his work on the GEA's development circle where, without fail for the last three years, he had met with the members of the team.</p>
</div>

<div class="page-paragraph">
    <p data-lang-id="002-richard-covid-battle">After battling Covid in January at the hospital (and overcoming it!), just as he was recovering, he got an infection that the doctors weren't able to treat.</p>

    <p data-lang-id="003-richard-dedication">His dedication has touched all of us trainers— and the global ecobrick community at large. His contribution helping to develop GoBrik and Ecobricks.org has touched hundred of thousands of ecobrickers around the world who have been inspired by the platforms, or who put the tools to use.</p>

    <p data-lang-id="004-richard-work-impact">For those ecobrickers who didn't know him personally, most likely you knew of him, or were touched by his work: tirelessly tweaking and fixing our authentication system, helping with validations and rejections— all to ensure that quality ecobricks were being made around the world, and that plastic would be kept out of the biosphere.</p>

    <p data-lang-id="005-richard-legacy">In a culture where we typically honor social status and accomplishments, it is worth noting the great ecological legacy Richard has left— and his journey to get there.</p>

    <p data-lang-id="006-richard-career-background">Richard spent the bulk of his career working for some of the largest international blue chip financial companies as a database designer. After retiring, whereas some would choose a quiet retirement minding their own business, Richard chose the other direction.</p>

    <p data-lang-id="007-richard-green-transition">As he began to feel more and more concern about the traditional financial system that he helped build, he also began to feel more and more concern for the plastic he was consuming. Determined to do something about both, he started his green transition by buying a Tesla— of which he was immensely proud! However, he bought it not so much for the ride (which he did love!) but, as he put it "to help energize and encourage humanity's green transition".</p>

    <p data-lang-id="008-richard-ecobricking-discovery">It was around then that he stumbled upon ecobricks and brikcoins.</p>

    <p data-lang-id="009-richard-uk-training">Richard joined our UK training of trainer program in Newforest UK where Ani, Lucie and I had the great pleasure to meet with him numerous times in person.</p>

    <p data-lang-id="010-richard-it-contributions">Not so much inclined to lead community workshops, he was nonetheless determined to help out with the community's IT needs. He put his extensive database skills to use helping us to manage the thousands of ecobrickers keen to log and record their hundreds of thousands of ecobricks. Whereas he could have used these skills to continue contracting on the side, he instead spent hundreds of hours helping us establish a nothing-short-of-miraculous online platform for the global plastic transition movement.</p>

    <p data-lang-id="011-richard-development-costs">Whereas other platforms that serve ~100,000 users cost millions of dollars to develop and maintain, with Richard's help our small development team of 3-5 volunteers, managed to do it with only a few thousands dollars over the last few years— funds that came directly from the Brikchain system of plastic offsetting that he was instrumental in helping setup.</p>

    <div style="width:100%">
        <img src="../webp/richard-system.webp" style="width:100%" data-lang-id="012-richard-api-system-image">
        <p style="font-size:0.9em; padding-left: 12px;" data-lang-id="013-richard-api-system-description">A dynamic flow chart of the API system that Richard setup that processes every ecobrick that is authenticated</p>
    </div>

    <p data-lang-id="014-richard-real-world-impact">All this of course had an immense real world impact (and continues to do so). The database and serial number system that he up set honors, energizes and encourages ecobricks to be made. Or in other words: keeping plastic out of the biosphere and out of carbon-intensive industrial facilities. Thanks to Richard's API system every single ecobrick that has been logged is recorded with its own serial number inscribed digitally and physical onto the ecobrick— now over two hundred thousand tiny mark's that Richard can take credit for— and the corresponding tons of plastic that are secured from polluting rivers, forest and fields around the world.</p>

    <p data-lang-id="015-richard-passing-impact">All this is to say that Richard's passing will leave a large hole that we'll struggle to fill over the next months and years.</p>

    <p data-lang-id="016-we-will-miss-richard">We will miss him dearly.</p>

    <img src="../photos/richard-circle.jpg" width="100%" alt="Richard in our ToT Talking circle" data-lang-id="017-richard-circle-image">

    <p data-lang-id="018-inspired-by-richard">But as we do so, we can be inspired by the great contributions, social and ecological, that are possible with quiet determination, focus and commitment.</p>

    <p data-lang-id="019-richard-commemoration">To commemorate Richard's passing we've updated Ecobricks.org to feature his last work: An algorithm that selects the ten ecobricks of the past month with the highest authentication scores to make a gallery.</p>

    <p data-lang-id="020-ecobricks-org-update">The gallery is now at the top of Ecobricks.org and has its own page at <a href="https://ecobricks.org/en/top-tens.php">https://ecobricks.org/en/top-tens.php</a> (now in English and Indonesian).</p>

    <p data-lang-id="021-gobrik-upgrade">Meanwhile, we're also working on upgrading GoBrik to also feature the last of his work-- another algorithm that shows the latest authenticated ecobricks.</p>

    <p data-lang-id="022-ecobrick-in-honor">In his honor, we'll be making an ecobrick in his honor this week. I will log mine with a prayer/vision for his passing on the system he helped develop using the plastic insertion technique he pioneered. I'll be tagging and posting this ecobrick on social media with #thanksRichard.</p>

    <p data-lang-id="023-sign-off">Russell, Lucie, Ani</p>

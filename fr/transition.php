            <p data-lang-id="054-manual-tracking-option">
            Alternatively, you can manually track and disclose your plastic generation yourself. Many ecobrickers do this with a two basket system. Over the course of the month, the plastic that they consume they clean, dry and put in a Weighing basket. At the end of the month, they weigh this plastic, and move it to their Ecobricking Basket. From this basket they grab the plastic for their ecobricks. Meanwhile, they begin the month anew filling the Weighing Basket with their consumption. As the month goes by, they also pay attention to the plastic they give away to others or use outside their home (produced plastic).</p>

            <p data-lang-id="055-monthly-net-plastic-consumption">Each month, GoBrik then calculates the net amount of plastic you‚Äôve consumed so far this year.</p>
            

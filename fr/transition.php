            <p data-lang-id="057-plastic-transition-score-percentage">The resulting Plastic Transition Score is then calculated as a percentage of zero waste.</p>

            <p data-lang-id="058-score-explanation">In other words, if you‚Äôre generating and ecobricking exactly the same amounts of plastic, then your score is 100 (i.e., 100% of zero waste). A PTR below 100 shows your household is still putting plastic into the biosphere. A score above 100 means you are removing plastic from the biosphere.</p>

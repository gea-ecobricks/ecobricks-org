						<p data-lang_id= 057-block-5-header-1>Earth is different everywhere on Earth!  Before we get started, we get to know our local earth.</p>
						<p data-lang_id= 058-block-5-paragrah-1>The first step is to test the earth that you will use for your construction.  Choose a soil near your building site with a high clay content, no stone, and no silt (decayed organic material). Ideally your earth sticks together nicely and can be formed into a ball that holds together.</p>
						<p data-lang_id= 059-block-5-paragrah-2>Of course, soil is different everywhere!  A soil’s character comes from the different amounts of sand, clay and silt in it.   If there’s too much clay in the earth, it will crack when it dries.  If there’s too much sand it will crumble when it dries.  If there’s do much silt, it won’t hold together nor be maleable like we need it to be.  We can however adjust our local soil to be better for building by adding more sand to it.  To do this, we  want to figure out the best combination of sand and earth to make your final building material– what we call ‘cob’.</p>
						<p data-lang_id= 060-block-5-paragrah-3>With your chosen earth, make some cob pancakes!  It’s just like baking with dirt when you were a kid, but more scientific!  Mix small batches of cob with varying ratios of your local sand and clay.   Make pancakes of about 3cm by 15cm.  Try different rations, like one part sand, two parts clay.  Lay out a mix of different rations (i.e.  1:0, 0:1, 1:3, 1:1, 3:1) on a board.   Let them dry in the sun and out of the rain.  Note that for this testing, we don’t yet add any organic binder (i.e. straw, hay) as we are solely focused on testing the earth.  The binder comes later and will add strength to the final cob.</p>
						<p data-lang_id= 061-block-5-paragrah-4>After two or three days, when the samples are fully dry, review your pancakes.  Break them carefully feeling how they hold up under pressure.</p>
						<p data-lang_id= 062-block-5-paragrah-5>Based on your review you are now able to make an educated decision on the ratio to use for your project’s cob!  Choose the ratio that does not crumble easily, nor which hasn’t cracked.  Of course, you’ll also want to factor in things like the transportation and cost of the sand or the cost.  Some cracking (especially for the interior of non-structural benches) is not a big deal– and remember, the binder we add will add strength.</p>
						<p data-lang_id= 063-block-5-paragrah-6>Once you have chosen your ideal ratio of sand and earth and have found an organic binder you’re ready to make cob!</p>

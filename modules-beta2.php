                    <h3><a href="https://www.ecobricks.org/wp-content/uploads/2019/01/tire-band-2.jpg"><img loading="lazy" class="alignright wp-image-39493 size-medium" src="https://www.ecobricks.org/wp-content/uploads/2019/01/tire-band-2-225x300.jpg" alt="" width="225" height="300"></a>Tire banding is a simple technique for joining ecobricks together using 100% re-used materials.</h3>
                    
                    <p>With luck, you have a mechanics shop near you that works with motor bikes.&nbsp; By using the old inner tubes from motor cycles we can also connect our ecobricks together.&nbsp; Unfortunately, bicycle and car tires are too small and too big for the straightforward application of this method.&nbsp; But you‚Äôre welcome to try ‚Äì it all depends on the diameter of your ecobricks and that of the inner-tube.</p>
                    
                    <p>Unlike old methods of tying a stack of ecobricks together with a long vertical strip of innertube, in the tire banding technique, we cut the tubes <strong>horizontally</strong> to make many small elastic bands.&nbsp; Just like the siliconing method, we‚Äôre not permanently bonding the ecobricks together, so that later the module can be fixed or destroyed without damaging the ecobricks.</p>
                    
                    <p>Like all ecobrick applications, the making process works best collaboratively.&nbsp; You‚Äôll need a partner, some ecobricks and a wooded stick.&nbsp;&nbsp; Your ecobrick packing stick or an <a href="/earthwand">earthwand</a> will do the trick!</p>
                    
                    <h4>Getting Started</h4>
                    
                    <p><a href="https://cdn.ecobricks.org/wp-content/uploads/2019/01/tire-band-diagram.png"><img loading="lazy" class="alignright wp-image-39529 " src="https://cdn.ecobricks.org/wp-content/uploads/2019/01/tire-band-diagram-300x211.png" alt="" width="345" height="243"></a>We‚Äôve found that motor cycle tires are ideal for binding 600ml bottles.&nbsp; Use only identical bottled ecobricks for one module.&nbsp; Really old inner tubes, or those that have been exposed to the sun won‚Äôt work ‚Äì they will snap too easily.</p>
                    
                    <ul>
                        <li>Take your scissors and cut across the inner tube to make a ring.&nbsp; As you cut the tube into bands, angle your cuts slightly so that the bottom of the band is slightly smaller than the top ‚Äì about 1 centimenter on the bottom and 1.3 cm on the top.&nbsp; This will help you maximize the number of bands you can get out of the tube, while minimizing waste.</li>
                        
                        <li>Cut only about six bands to start as you will need to test the dimensions/elastacity of your bands before you cut the rest of the tire.</li>
                        
                        <li>Hopefully you‚Äôve follow the recommended ecobrick <a href="/how">making and storage methods</a> so that you‚Äôre ecobricks have colored bottoms and are easily accessible.&nbsp; Arrange your ecobricks to make a pattern of colored bottoms (this will be the top of your module).</li>
                        
                        <li>Slide a band over the bottom and the top of your center ecobrick.&nbsp; Bring out the adjacent ecobrick.&nbsp; Slide the top and bottom elastics over the second ecobrick so that it is partnered with the first.</li>
                        
                        <li>Now connect your second ecobrick, in the same way to a third.&nbsp; In this way you‚Äôll have three partners attached in a line.</li>
                        
                        <li>Push all three bottles together and connect bottle the first bottle, to the third.&nbsp; The ecobricks should be tightly bonded in a triad!&nbsp; They should not be able to slide back and forth or wiggle.&nbsp; If your bonds are too loose (or too tight!), go back to cutting your elastics.&nbsp; By adjust the thickness of your elastic you can adjust the tightness of your tube bonds.</li>
                        
                        <li>Once you‚Äôve got a good feel for the sizing of your bands, cut the entire up into bands.&nbsp; You‚Äôll eventually be able to put them all to use.&nbsp; See if you can do it without creating any waste snippets!&nbsp; Of course‚Ä¶ every bit of excess cutting, you can put into an ecobrick (like the nozzle)!&nbsp; <strong>This makes this a zero-waste making method.</strong></li>
                        
                        <li>Alright‚Ä¶ that was the easy part!&nbsp; Now, connect ecobrick No. 4 to ecobrick No.3.&nbsp;&nbsp; Connect No. 4 to No.2‚Ä¶. Continue to add on ecobricks in this way.&nbsp; It won‚Äôt be easy, as the bonds will start to get tight and overlap.&nbsp; This is where working with a partner comes in handy.</li>
                        
                        <li>Have one partner hold the connected ecobricks and another adding the new ecobrick.&nbsp; Use a stick or <a href="/earthwand">earthwand</a> to press down the elastics&nbsp; as you go.</li>
                        
                        <li>Continue adding ecobricks until you‚Äôve completed your triangle or hexagon module.</li>
                        
                        <li>Use your stick to level out the elastics on the side so that they make a neat, level line around the outside.</li>
                        
                        <li>And, that‚Äôs it!&nbsp; Your module is good to go!</li>
                    </ul>
                    
                    <p><a href="https://www.ecobricks.org/wp-content/uploads/2020/07/Tire-Banded.jpg"><img loading="lazy" class="wp-image-65361 size-medium alignright" src="https://www.ecobricks.org/wp-content/uploads/2020/07/Tire-Banded-300x298.jpg" alt="" width="300" height="298"></a>If you have used bottles of identical brand, the hexagon module top will fit perfectly into a triangular module bottom.&nbsp; Make more modules!&nbsp; Experiment with module size and back-to-back modules.&nbsp; Make cool stuff!</p>

                    

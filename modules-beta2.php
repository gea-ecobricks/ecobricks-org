                    <p>If there‚Äôs anything that the very different earth building traditions around the world have in common it is that they used what was local to them.  Earth building makes use of the earth (sand, clay, rock, pebbles, etc.) that is nearby along with the local organic materials (wood, bamboo, reeds, grasses, etc).  By keeping it local, we reduce the need for long haul transportation.  This ensures a low-energy impact footprint for our building and it encourages the community management of regrowing trees and bamboo to meet local needs.</p>

                    <p>There‚Äôs an added bonus.  As we‚Äôre using the same resources that our neighbours have access to, they can copy us! As we all work with the same resources, we can learn from the trial and error of our neighbours, and they can learn from us.  Sure and steady, our methods get better and greener.  Since our shared resources come from nearby,  we can also work together in the regrowth and management of resources.</p>
          
                    <p>Today‚Äôs commercial construction, in contrast, relies on industrial processes and the transportation of materials from far away.  Often these materials involve much energy in their fabrication and transportation.  Because we don‚Äôt get to see where they come from, we are often not aware of the mining, smelting, and energy expenditure behind them.</p>

            <div class="reg-content-block" id="block3">

                <div class="opener-header">
                    
                    <div class="opener-header-text">
                    <h4>Silicone Method</h4>
                    <h5>The original and most enduring making milstein modules is using silicone sealant.</h5>   
                    <br>
                    </div>

                    <button onclick="preclosed3()" class="block-toggle" id="block-toggle-show3">+</button>
                </div>

                <div id="preclosed3">
                
                <br>

                <p>Normal, everyday silicone sealant can be used to make mistein modules.  This is the orginal way to make modules, by using silicone sealant.  Builder‚Äôs silicone, also known as ‚Äúcaulking‚Äù and ‚Äúaquarium sealant‚Äù is inexpensively found in hardware stores around the world.  Silicone is non-petroleum.  It is not a type of plastic, and it does not degrades into microplastics, nor leach toxins or interfere with ecological cycles.  The HDPE plastic bottle can be easily recycled and/or used in your projects.  See the chart at the bottom of the page on how this method compares with others.</p>
                <h4>Preparation</h4>
                <p>To start you will need a well ventilated space, a perfectly flat surface, a caulking gun, a tube of silicone, a knife and of course, your ecobricks.  Hopefully, you‚Äôve followed <a href="/how">GEA ecobrick making and storage directions</a>, so that you can easily pick a selection of colored ecobricks to make a cool pattern.</p>
                
                <p>There are many different types of silicone ‚Äì our experience is that the cheapest all-purpose product (<strong>not </strong>special-brand or special purpose version) at your local hard ware store works best. Avoid colored silicones and stick to clear.</p>
                
                <h4>Safety</h4>
                
                <p>Make sure you‚Äôre working outside or with a fan, because as silicone cures it releases vinegar gas (acetic acid).&nbsp; It‚Äôs not good for you or your helpers if you breathe too much of this.&nbsp; Ideally, have a partner assist:&nbsp; one person holds and places the ecobricks, the other operates the silicone gun.</p>
                
                <div id="attachment_1290" style="width: 215px" class="wp-caption alignright"><a href="https://cdn.statically.io/img/www.ecobricks.org/f=auto%2Cw=800%2Ch=600%2Cq=75/wp-content/uploads/2014/09/14-illustration-1.jpg"><img aria-describedby="caption-attachment-1290" loading="lazy" class="wp-image-1290" src="https://cdn.statically.io/img/www.ecobricks.org/f=auto%2Cw=800%2Ch=600%2Cq=75/wp-content/uploads/2014/09/14-illustration-1-300x212.jpg" alt="Lay bottles on a perfectly flat surface. Ensure all bottles are the same size. Arrange a color pattern with caps and bottle bottoms. Silicone junction points. Press together. Let dry 25 hours." width="205" height="145"></a>
                
                <p id="caption-attachment-1290" class="wp-caption-text">Use silicone sealand sparingly to bond bottles</p></div>
                
                <h4>To Start‚Ä¶</h4>
                
                <ul>
                    <li>Go through your available ecobricks and create a beautiful pattern using the bottom colors of the ecobricks.&nbsp; Arrange you ecobricks in the desired module pattern with the bottom down.</li>
                    <li>When you‚Äôre pattern is set, remove the cap of the center ecobrick (this will help guide you as the module grows).</li>
                    <li>Insert the silicone tube into your caulking gun and use your knife to cut the top off your silicone tube and the nozzle.&nbsp; Be sure to ecobrick the plastic cuttings!</li>
                </ul>
                
                <h4>Siliconing</h4>
                <ul>
                    <li>One partner removes the center ecobrick from the test arrangement and holds it for the other to silicone.</li>
                    <li>Lay a single thick a bead of silicone down the side of the ecobrick.&nbsp; The bead should be high, not squished or flat ‚Äì this will help create the strongest bonds between bottles.</li>
                    <li>Place the bottle down in the center of the flat space you‚Äôve chosen.&nbsp; Take the next ecobrick, and press it gently against your center bottle.&nbsp; And your off!</li>
                    <li>Grab the third bottle.&nbsp; This time add two beads of silicone down the sides of the bottle (where the bottles will touch the two other).&nbsp; Gently press this bottle against the first two.</li>
                    <li>Ideally your high silicone beads compress nicely, maximizing the contact surface.</li>
                    <li>Continue adding ecobricks in this way, from the center outwards.</li>
                    <li>Be sure as you press new bottles into the pattern that you do not disturb the bonds of the earlier bottles.&nbsp; Try to avoid repressing bottles together after they‚Äôve been set.&nbsp; Undisturbed silicone joints are strongest.</li>
                    <li><strong>Be careful as you work to not lean over the pattern</strong>‚Äî the vinegar fumes from the silicone are strongest here.&nbsp; Always work from around the outside.</li>
                </ul>
                <h4>Finishing up</h4>
                <ul>
                    <li>Once all your bottles are all laid, its time to‚Ä¶ <strong>wait</strong>!</li>
                    <li>Do not move or even touch your module for the next two hours.&nbsp; After two hours, you can safely put the cap back on the center bottle and if necessarily gently slide the modules (but its best not to!).</li>
                    <li>After 24 hours you can move the module.</li>
                    <li>After 36 hours (i.e. when you can no longer small the silicone drying smell) your module is good to sit and even stand on!</li>
                </ul>


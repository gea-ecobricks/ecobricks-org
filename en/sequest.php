                <h4 data-lang-id="007-title">Theory & Science</h4>
                <h5 data-lang-id="008-subtitle">The goal of plastic sequestration is to follow Earth's example of carbon care in preventing the degradation dispersal of plastic into the biosphere‚Äî and in keeping it out of industry.</h5>

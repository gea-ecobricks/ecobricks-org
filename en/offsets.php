            <p data-lang-id="005-first-page-paragraph">And plastic offsetting is a place to start!</p>

				<p data-lang-id="006-second-page-paragraph">Offsetting is all about making sure our green impacts outweigh our grey. Just as keen green households and companies buy carbon offset credits, so too can they buy plastic offset credits.  Whereas carbon credits help balance off our emissions, plastic offsets can be purchased to balance our plastic consumption and production.  Paying for offset credits serves as strong financial incentive for buyers to reduce and eliminate their plastic use altogether.</p>
                    
                <p data-lang-id="007-third-page-paragraph">Meanwhile, payments go direct to energizing those working hard to sequester plastic and transition from it.</p>  


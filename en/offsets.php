                <p data-lang-id="024-block-1-paragraph">To calculate the price of 1kg of authenticated plastic, the Global Ecobrick Alliance divides its costs of operating and maitaining the blockchain by the amount of plastic that has been authenticated.</p>

                <p data-lang-id="025-block-1-paragraph">For example: In 2023 the Global Ecobrick Alliance incurred 7,207.14 $ USD of expenses in order to authenticate 2,228.96 Kg AES of ecobricked plastic. That works out a cost of 3.23 $ USD per Kg in maintaining the block chain and authentication system. This sets our valuation of the price per Kg of AES plastic offsets for the subsequent year (2024).</p>

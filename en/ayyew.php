                <h4 data-lang-id="009-ayyew-title">An ancient ecological ethos for our modern moment</h4>

                <p data-lang-id="010-page-paragraph">Like many other indigenous peoples around the world, <a href="https://medium.com/earthen/1000-years-pollution-free-d8ddb70a703f" target="_blank">the concept of waste does not exist</a> in the Igorot language prior to the arrival of European colonizers.  All the materials that Igorots used, had their own cycles of use and reuse.  For the Igorots the cycles of life around them were so important, that their culture was centered upon them.  To this day, <a href="https://book.earthen.io/en/kincentric">cyclocentric Igorot society</a> continues to thrive‚Äî the forests and terraced hills of their land being some of the most verdant and biodiverse in the Philippines.</p>.

                <p data-lang-id="011-page-paragraph">Underlying Igorot cyclocentric culture is their concept of <i>ayyew</i>‚Äî a term that has no corollary in Latin languages or Western philosophy.</p>

                <p data-lang-id="012-page-paragraph">For the Igorots ayyew is a virtue to be cultivated and appreciated in the same way that courage, honor, or wisdom. Whereas Western virtues tend to be social, ayyew is ecological.  As one of their guiding values, ayyew directs the way Igorots go about living their lives‚Äî in particular their relationship to the animals, plants, birds and bugs around them.</p>

                <p data-lang-id="016-page-para">In other words, being ayyew refers to the cyclical contribution a person is adding to a particular cycle (like that of a rice paddy, oak forest or cabbage garden).  In this way, a person, household or community is admired and respected to the degree in which they are enriching the cycles of life of which they are part.</p>

                <p data-lang-id="013-page-paragraph">When Igorots refer to a person or process being "ayyew" they are referring to how well that person is in sync with the cycles of life around them.  When someone is ayyew, it means that they are moving towards harmony with ecological cycles.  If someone is not ayyew, well, that means they are jamming things up and causing trouble for the plants and animals, fields and forests around them.</p>

                <p style="margin-left:15px; margin-top:20px; border-left:2px solid var(--text-color);padding-left:10px;"><i data-lang-id="014-ta-quotation">All aspects of Igorot life and culture are guided by the virtue of ayyew. Men, women, households and communities are admired and respected to the degree in which they embody the principle. Ayyew means to not just to be in sync with a cycle, but to tend to its spin.</i></p>
                <p style="font-size:small;text-align:right;margin-top:-8px;margin-bottom:30px;" data-lang-id="015-ta-quotation-caption">
                    ‚Äî Banayan & Maier, <a href="https://book.earthen.io/en/spirals.html">Tractatus Ayyew, Cycles that Spiral</a></p>



                <p data-lang-id="017-page-para">One of the best ways to understand the concept of ayyew is how Igorots manage their food.  To begin with, Igorot villages are almost completely self-sustaining‚Äî almost all their food grown in the valleys around them.  How they grow their food, eat it and throw away left-overs (burned rice, vegetable cuttings, husks and shells, etc.) illustrates a varying level of ayyew...</p>

                <ul>
                <li data-lang-id="018-page-para">Throwing food leftovers into a garbage can would not be  ayyew as it causes a mess for humans and doesn't help the field or animals when it is dumped in a mixed pile!</li>
                <li>Composting food leftovers would be ayyew as it contibutes to the garden that provided the food in the first place.</li>
                <li>More ayyew would be to feed the leftovers to the family chickens, who, well fed provide eggs in return</li>
                 <li>And even more ayyew would be to give the leftovers to the family pig, who grows fat and provides even richer fertilizer for the garden (and meat later on!).</li>
                 </ul>

                <p data-lang-id="019-page-para">The way that land is managed provides another example of Ayyew...</p>

                <ul>
                    <li data-lang-id="020-page-para">A farmer who turns a barren hillside into a cabbage field is being ayyew.  In doing so, he has added several new species to the space and is generating sustenance for themselves and the community.</li>
                    <li>A farmer who turns a cabbage field into a rice padding is also being ayyew.  In doing so, water is captured and pooled to grow not just rice, but provide home for snails, frogs, mudfish and more.</li>
                      <li>A gardener, who plants a paddy with fruit trees is also being ayyew.  In doing so sustenance is provided for not just humans, but birds and bugs of all kinds</li>
                      <li>A man or woman who protects the verdant oak forests on the top of the local hills is even more ayyew‚Äî for what has more cycles of life than a forest?</li>
                   </ul>

                <p data-lang-id="021-page-para">In this way, being ayyew is not a state of being, but <i>a process of being</i> that has a direction: a way of living that leads to ever more abundance from ever more sync with the surrounding cycles of life.</p>


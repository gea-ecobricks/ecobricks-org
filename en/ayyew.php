                <p data-lang-id="062-direct-equivalent-description">Ayyew is the virtue of striving to be in ever more harmony with the surrounding ecological cycles.  Ayyew is not a state of being, but a process of being.  However, it is a directional process, one that leads to ever more abundance from ever more sync with the surrounding cycles of life.</p>

                <p data-lang-id="063-good-thing-description">Ayyew is a virtue or a ‚Äúgood thing‚Äù for Igorots, something that is good for individuals, households and community to strive for. In the same way as individual voices in a choir merge to attain otherwise impossible harmonies, their lives are integrated with the cycles of other species to achieve otherwise impossible health and abundance.</p>

                <p data-lang-id="064-non-judgemental-description">Because Ayyew is not a state but rather a process, it is non-judgemental of one‚Äôs present ecological state.  In this way, an industrial farmer with a giant mono-crop could be also be Ayyew (assuming they‚Äôve shifted from striving from profit, to striving for ecological harmony.)</p>

                <p data-lang-id="065-perspective-plastic-description">Likewise, from an Ayyew perspective plastic is not bad in and of itself.   It is a material with certain properties and characteristics that can be put to use in different ways.</p>

                <p data-lang-id="066-language-description">Interestingly enough, in the Igorot language, there was never a word for ‚Äúwaste‚Äù or ‚Äútrash‚Äù*.  From the ayyew perspective, nothing is innately useless.  Instead, every material presents an opportunity that can be embraced‚Äì or not.</p>


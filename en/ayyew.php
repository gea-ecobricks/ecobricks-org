                <p data-lang-id="025-page-para">Ayyew is a virtue or a ‚Äúgood thing‚Äù for Igorots, something that is good for individuals, households and community to strive for.</p>

                <p data-lang-id="026-page-para">Because Ayyew is not a state but rather a process, it is non-judgemental of one‚Äôs present ecological state.  In this way, an industrial farmer with a giant mono-crop could be also be Ayyew!  Assuming they‚Äôre making a shift that is: switching from mono-cropping to polycropping, ditching pesticides, moving on from chemical fertilizer, etc.  While these are simple shifts, in so far as they moving in the direction of alignment with ecological cycles, they are being ayyew.</p>

                <p data-lang-id="027-page-para">This non-judgemental perspective, has important implications for the way Igorots manage their plastic.  From an Ayyew perspective the plastic that passes through their hands each day is not bad in and of itself.   Rather, it is a material that reflects our collective moment in time and which has its own unique properties.  Properties that can be put to use in different ways that either enrich or deplete ecological cycles.</p>-->

                <p data-lang-id="028-page-para">From the Ayyew perspective, materials are neither good nor bad‚Äî it all depends on how one puts them back to use in synk (or in conflict) with the cycles of life.  Consequently, in the Igorot language, there was no word for ‚Äúwaste‚Äù or ‚Äútrash‚Äù to condemn a material as 'used-up' or 'useless'.   Instead, every material presents an ecological opportunity that can be embraced‚Äì or not.</p>

                <p data-lang-id="029-page-para">Including plastic.</p>

                <p data-lang-id="030-page-para">From this ayyew perspective, ecobricking arose in Igorot villages as a way to put plastic to good, green use.</p>

                <p data-lang-id="031-page-para">Rather than burning or dumping plastic (where it contaminates rivers and fields) Igorots observed it was much better to keep it in its solid state.  Rather than send plastic bottles off to be burned, recycled or dumped somewhere else, far more ayyew was to put it to use locally in its solid state.  From this perspective, it was a short leap to packing and securing plastic into PET bottles‚Äî and then in the stone building Igorot tradition, put these "bottle bricks" to use to build gardens and walls.</p>

                <p data-lang-id="032-page-para">Of course, it's possible to strive and sync even better with the surrounding ecological cycles.  An ayyew ecobricker strives to move from modules to gardens to <a href = "earth.php">earth and ecobrick</a> green space structures that <a href = "sequest.php">sequester plastic</a> and CO2.</p>

                <p data-lang-id="033-page-para">Inspired by this deep and powerful Igorot concept, the Global Ecobrick Alliance has developed our principles, techniques and applications around the concept of Ayyew.   Ayyew fits in closely with regenerative philosophy and guided our development of the <a href = "https://www.ecobricks.org/principles">regenerative guiding principles</a>. that underlie the work of the <a href = "about.php">Global Ecobrick Alliance</a> and the <a href = "movement.php">global ecobrick movement</a>.</p>


                <h3 data-lang-id="034-guiding-title">A Guiding Green Principle</h3>


                <p data-lang-id="035-page-para">The concept of ayyew has great relevance for us in the ecological downturn of the 21st century as we strive to solve ecological challenges.  Moving on from linear, black and white judgements, we can shift instead to acceptance.  And from there, steadily increase our alignment and contribution to ecological cycles.</p>

                <p data-lang-id="036-page-para">Ecobricking is just one modern manifestation of the Ayyew concept.</p>

                <p data-lang-id="037-page-para">Building on the concept of Ayyew, the co-founders of the Igorot ecobrick movement have gone on to develop a theory of what green should really mean.  Their <i>Tractatus Ayyew</i> lays out a theory or Earthen ethics using plastic as an example for how ayyew can help us solve our modern ecological challenges.</p>



                <h4 data-lang-id="009-ayyew-title">An ancient ecological ethos for our modern moment</h4>

                <p data-lang-id="010-page-paragraph">Like many other indigenous peoples around the world, <a href="https://medium.com/earthen/1000-years-pollution-free-d8ddb70a703f" target="_blank">the concept of waste does not exist</a> in the Igorot language prior to the arrival of colonists.  All materials and objects in Igorot use had its own cycles of various phases of use.  The cycles of life, of matter, and of the seasons were so important to the Igorots, that their culture was centered upon them.  To this day, <a href="https://book.earthen.io/en/kincentric">cyclocentric Igorot society</a> continues to thrive-- their fields and forest being some of the most verdant and biodiverse in South East Asia, while their innovations (like ecobricking) have inspired tens of millions of folks around the world.</p>.

                <p data-lang-id="011-page-paragraph">Underlying Igorot cyclocentric culture is their concept of <i>ayyew</i>‚Äî a term that has no corollary in latin languages or western philosophy.</p>

                <p data-lang-id="012-page-paragraph">As one of the chief virtues of the Igorot people, the concept of Ayyew guides the Igorot way of life.   In Igorot culture, Ayyew is as important and as lauded as other virtues such as Courage, Honor or Loyalty..</p>

                <p data-lang-id="013-page-paragraph">When Igorots refer to a person or process being "ayyew" they are referring to how well that person is in sync with the cycles of life around them.  When someone is ayyew, it means that they are harmonizing with ecological cycles.  If someone is not ayyew, well, that means they are jamming things up and causing trouble!</p>

                <p style="margin-left:15px; margin-top:20px; border-left:2px solid var(--text-color);padding-left:10px;"><i data-lang-id="014-ta-quotation">All aspects of Igorot life and culture are guided by the virtue of ayyew. Men, women, households and communities are admired and respected to the degree in which they embody the principle. Ayyew means to not just to be in sync with a cycle, but to tend to its spin.</i></p>
                <p style="font-size:small;text-align:right;margin-top:-8px;margin-bottom:30px;" data-lang-id="015-ta-quotation-caption">
                    ‚Äî Banayan & Maier, <a href="https://book.earthen.io/en/spirals.html">Tractatus Ayyew, Cycles that Spiral</a></p>

                <p data-lang-id="016-page-para">In other words, being ayyew refers to the cyclical contribution a person is adding to a particular cycle.  In this way, a person, household or community is admired and respected to the degree in which they are enriching the cycles of life of which they are part.</p>

                <p data-lang-id="017-page-para">One of the best examples of ayyew is dealing with food left-overs.  Igorot villages are almost completely self-sustaining-- with almost all their food grown in the valleys around them.  Consequently, there are various ways to manage left-overs from a meal (burned rice, vegetable cuttings, husks and shells, etc.).  Each method, represents a varying level of ayyew...</p>

                <p data-lang-id="018-page-para">Throwing food leftovers randomly away would not be consider ayyew-- as it causes a mess! Composting food leftovers would be ayyew as it contibutes to the garden that provided the food in the first place. However, even more ayyew would be to feed the leftovers to the family pigs or chickers, who would both grow fat and provide even richer fertilizer for the garden.</p>

                <p data-lang-id="019-page-para">The way that land is managed provides another example of Ayyew...</p>

                <p data-lang-id="020-page-para">A farmer who turns a barren hillside into a cabbage field is being ayyew.  In doing so, he has added several new species to the space and is generating sustenance for themselves and the community.  A farmer who turns a cabbage field into a rice padding is also being ayyew.  In doing so, water is captured and pooled to grow not just rice, but provide home for snails, frogs, mudfish and more.  A forester, who plants a paddy with fruit trees is also being ayyew.  In doing so sustenance is provided for not just humans, but birds and bugs of all kinds as the canopy of the trees provides even more net volume for life to make its home and live in cycles.  Finally, those who provide and overseeing the forestation of an orchard are also being ayyew (or simply protecting a forest)-- for what has more cycles of life than a forest?</p>

                <p data-lang-id="021-page-para">In this way, being ayyew is not a state of being, but a process of being.  However, it is a directional process, one that leads to ever more abundance from ever more sync with the surrounding cycles of life.  Consequently, being ayyew, can include very low-level and simple shifts, as one ascends the steps of ecological synchrony and enrichment.</p>


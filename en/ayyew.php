                <p data-lang-id="005-first-page-paragraph">The Igorots, one of the few unconquered indigenous people of South East Asia, have thrived for centuries in the mountains of Northern Luzon in the West Philippines Sea. While over the last 300 years, Spanish, Japanese and Americans colonized the Philippines archipelago, they were unable to dominate the various Igorot tribes that lived in the fertile valleys of the mountainous region.</p>

